# Forskjellen på lossy komprimering og lossless komprimering er at lossy fjerner unødvendiginformasjon som er vanskelig for blåtte øye å oppdage 
# mens lossless gjenskaper fullverdigdata som komprimeres.
# En lossy komprimering vil komprimere tilsvarende lik informasjon,feks like pixler i et bilde eller tar de samme nyansene i en video.
# 
# Run-length encoding kan brukes ved at man komprimerer flere like data samlet. Feks et bildemed størst andel svart skjerm og et gult smilefjes i midten 
# gjøres om til korte koder somtilsvarer rekker med kortere verdier som tilsvarer det fulle bildet. Dette gjør det mulig å oppnålossless komprimering.